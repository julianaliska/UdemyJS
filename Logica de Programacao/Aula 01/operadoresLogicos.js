/*
* Operadores Lógicos
* && -> AND -> E -> Todas as expressões precisam ser verdadeiras para retornar true
* || -> OR > OU
* ! -> NOT -> NÃO
*/


let num1 = 5;
let num2 = 10;

console.log(10 > 5 && 10 == 10); //Retorna true pois todas são verdadeiras

console.log(10 > 5 || 10 < 5); //Retorna verdadeiro pois a primeira expressão é verdade

console.log(!true); //A exclamação inverte a operação, imprimiu o false