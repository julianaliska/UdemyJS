/*
* If
* Else if - Posso ter várioa else if na checagem
* else - Só pode ter um else por último 
*/

const hora = 12;

if (hora >= 0 && hora <= 11) {
    console.log('Bom dia');
} else if (hora >= 12 && hora <= 17){
    console.log('Boa tarde');
} else if (hora >= 18 && hora <= 23){
    console.log('Boa tarde');
}