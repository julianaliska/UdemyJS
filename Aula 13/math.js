let num1 = 9;
let num2 = Math.floor(num1); //Arredonda o valor para baixo 
let num3 = Math.ceil(num1); //Arredonda o valor para cima
let num4 = Math.round(num1); //Arredonda para o número mais próximo automaticamente

console.log(num2, num3, num4); 

console.log(Math.max(1,2,3,4,5,6)); //Retorna o maior número entre os parentêses
console.log(Math.min(1,2,3,4,5,6)); //Retorna o menor número entre os parentêses

console.log(Math.random()); //Retorna um número aleatório entre 0 e 1.

let aleatorio = Math.round(Math.random() * (10 - 5) + 5); //Irá retornar um número aleatório entre 10 e 5 
console.log(aleatorio);

console.log(Math.PI); //Retorna o valor de PI 
console.log(Math.pow(2, 10)); // Eleva o 2 na 10. 
console.log(num1 ** 0.5); //Retorna a raiz quadrada do número

